/**
 * Given an age as input
 * if greater than 18 print "Adult"
 * else print "Not an adult"
 * */

public class CondicionaisAula2 {
    public static void main(String[] args) {

        int age = 17;

        if (age > 18) {
            System.out.println("Adult");
        } else {
            System.out.println("Not an Adult!");
        }
    }
}
